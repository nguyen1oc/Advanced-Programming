#include <iostream>
#include <math.h>
#include <string>

int main()
{
    int x;
    std::cin>>x;
    std::cout<<"Abs: "<<std::abs(x)<<"\n";
    std::cout<<"Sin: "<<std::sin(x)<<"\n";
    std::cout<<"Cos: "<<std::cos(x)<<"\n";
    std::cout<<"Tan: "<<std::tan(x)<<"\n";
    std::cout<<"Log: "<<std::log(x)<<"\n";
    std::cout<<"Log10: "<<std::log10(x)<<"\n";
    std::cout<<"Sqrt: "<<std::sqrt(x)<<"\n";
    std::cout<<"Exp: "<<std::exp(x)<<"\n";
    return 0;
}
